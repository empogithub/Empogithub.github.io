self.addEventListener('install', event => {
  event.waitUntil(
    caches.open('surf-deluxe-cache').then(cache => {
      return cache.addAll([
        './',
        './index.html',
        './style.css',
        './script.js',
        './manifest.json',
        './IMG_8308.jpeg',
        './IMG_8308.jpeg'
      ]);
    })
  );
});

self.addEventListener('fetch', event => {
  event.respondWith(
    caches.match(event.request).then(response => {
      return response || fetch(event.request);
    })
  );
});